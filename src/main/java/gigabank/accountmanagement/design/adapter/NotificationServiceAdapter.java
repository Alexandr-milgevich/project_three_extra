package gigabank.accountmanagement.design.adapter;

/**
 * Интерфейс адаптера для отправки уведомлений.
 * Определяет общий контракт для сервисов, реализующих отправку SMS и email.
 * Реализации этого интерфейса должны предоставлять конкретную логику
 * взаимодействия с внешними системами или сервисами уведомлений.
 * Используется для обеспечения гибкости и возможности легкой замены
 * реализации механизма отправки уведомлений.
 */
public interface NotificationServiceAdapter {

    /**
     * Отправляет SMS-сообщение на указанный номер телефона.
     *
     * @param phone   Номер телефона получателя в международном или национальном формате.
     * @param message Текст сообщения для отправки.
     */
    void sendSms(String phone, String message);

    /**
     * Отправляет электронное письмо на указанный email-адрес.
     *
     * @param email   Адрес электронной почты получателя.
     * @param subject Тема письма.
     * @param body    Содержимое письма (может быть plain text или HTML).
     */
    void sendEmail(String email, String subject, String body);
}