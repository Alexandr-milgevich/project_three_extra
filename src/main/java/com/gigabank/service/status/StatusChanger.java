package com.gigabank.service.status;

/**
 * Интерфейс для изменения статуса отдельной сущности по её идентификатору.
 * Реализуется сервисами конкретных сущностей (User, Account, Transaction).
 *
 * @param <T> тип сущности (используется для документирования, не является параметром метода)
 * @param <S> тип статуса сущности (enum)
 */
public interface StatusChanger<T, S> {

    /**
     * Изменяет статус конкретной сущности по её идентификатору.
     * Реализация должна быть транзакционной (аннотация @Transactional на уровне реализации).
     *
     * @param id        идентификатор сущности для изменения статуса
     * @param newStatus новый статус для установки
     */
    void changeStatus(Long id, S newStatus);
}