package com.gigabank.service.status;

/**
 * Интерфейс для обновления статусов связанных сущностей.
 * Реализуется сервисами, которые обновляют статусы дочерних объектов при изменении
 * статуса родительской сущности.
 *
 * @param <T> тип коллекции сущностей для обновления (обычно List<Entity>)
 * @param <S> тип статуса, на который нужно обновить
 */
public interface StatusUpdater<T, S> {

    /**
     * Обновляет статусы группы связанных сущностей при изменении статуса родительской сущности.
     * Реализация должна быть транзакционной (аннотация @Transactional на уровне реализации).
     *
     * @param entities  коллекция сущностей для обновления статусов
     * @param newStatus новый статус для установки
     */
    void updateStatus(T entities, S newStatus);
}